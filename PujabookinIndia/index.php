<script>
function sharePuja(url, title) {
    // Construct the full URL
    const fullUrl = window.location.origin + '/puja-details.php?url=' + encodeURIComponent(url);
    const shareText = `Check out this amazing ${title} event! Book now at ${fullUrl}`;

    // Check if Web Share API is supported
    if (navigator.share) {
        navigator.share({
            title: title,
            text: shareText,
            url: fullUrl
        })
        .then(() => console.log('Shared successfully'))
        .catch((error) => console.log('Error sharing:', error));
    } else {
        // Fallback: Copy link to clipboard and alert user
        navigator.clipboard.writeText(shareText)
            .then(() => {
                alert('Link copied to clipboard! Paste it into your favorite app to share.');
            })
            .catch((error) => {
                console.log('Clipboard error:', error);
                alert('Sharing not supported. Please copy this manually: ' + shareText);
            });
    }
}
