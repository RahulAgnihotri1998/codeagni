<script>
    document.addEventListener("DOMContentLoaded", function () {
        let tabs = document.querySelectorAll(".tab__btn");
        let contents = document.querySelectorAll(".tab__content");
        let autoSwitch = true;
        let currentIndex = 0;
        let intervalTime = 4000; // 4 seconds
        let interval;

        function showTab(index) {
            tabs.forEach(tab => tab.classList.remove("active"));
            contents.forEach(content => {
                content.classList.remove("open");
                content.style.display = "none";
            });

            tabs[index].classList.add("active");
            contents[index].style.display = "block";

            // Smooth fade-in effect
            setTimeout(() => {
                contents[index].classList.add("open");
            }, 10);
        }

        function startAutoSwitch() {
            interval = setInterval(() => {
                if (autoSwitch) {
                    currentIndex = (currentIndex + 1) % tabs.length;
                    showTab(currentIndex);
                }
            }, intervalTime);
        }

        tabs.forEach((tab, index) => {
            tab.addEventListener("click", function () {
                autoSwitch = false; // Stop auto-switching on user interaction
                clearInterval(interval);
                showTab(index);
            });
        });

        // Initialize first tab
        showTab(currentIndex);
        startAutoSwitch();
    });

</script>
